# # El programa "Hola Mundo"
# El programa **"Hola Mundo"** es frecuentemente el primer programa que se escribe para aprender un nuevo lenguaje de programación y probar su sintaxis básica. El programa simplemente imprime en la pantalla la hilera de texto "Hola Mundo" o una similar.
# 
# La tradición del uso de este programa en el aprendizaje de la programación de computadoras tiene su origen en el libro [_The C Programming Language_](https://en.wikipedia.org/wiki/The_C_Programming_Language), escrito en 1978 por Bryan W. Kernighan y Dennis M. Ritchie. El programa de ejemplo de este libro imprime la hilera en inglés **"hello, world"** (en minúscula, con una coma entre las palabras y sin signos de admiración). Aunque este el texto que le dio popularidad, el programa había sido utilizado previamente en el [tutorial del lenguaje de programación B](https://www.bell-labs.com/usr/dmr/www/bintro.html), de Bryan W. Kernighan.
# 
# El sitio [The Hello World Collection](http://helloworldcollection.de/) presenta el código de "Hola Mundo" en aproximadamente 600 lenguajes de programación.

